// Programar em Python #05 - Variáveis

using System;

namespace Base {
    class Variaveis {
        public void Run() {
            int _int = 10;
            double _double = 10.5;
            string _string = "Eu sou uma string!";
            char _char = 'c';
            bool _bool = true;

            Console.WriteLine(_int);
            Console.WriteLine(_double);
            Console.WriteLine(_string);
            Console.WriteLine(_char);
            Console.WriteLine(_bool);
        }
    }
}